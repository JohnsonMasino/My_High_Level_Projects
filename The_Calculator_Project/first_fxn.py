#!/usr/bin/python3

#This first file contains an addition funtion for the 'numi-calculator_clone' project

def adding0(a, b):
    """this module is called when the user tries to sum only two
    integers and uses '+' as the summation call symbol
    """
    return a + b

def subtracting0(a, b):
    """this module is called when the user tries to subtract only two
    integers and uses '-' as the summation call symbol
    """
    return a - b

def multiplying0(a, b):
    """this module is called when the user tries to multiply only two
    integers and uses '*' as the multiplication call symbol
    """
    return a * b
